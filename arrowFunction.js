// Declaring an Arrow Function with a single parameter.
// This Arrow Function will take a single input and divide the value by 5 and give the output.

const dividedByFive = number => number / 5;

console.log(dividedByFive(50));


// Declaring an Arrow Function which will take two input parameter and add 2 to both of the parameter and multiply them.
const sum = (first, second) => (first + 2) * (second + 2);
console.log(sum(10, 20));


// Declaring an Arrow Function which will take three input parameter and multiply them.
const multiply = (firstNumber, secondNumber, thirdNumber) => firstNumber * secondNumber * thirdNumber;
console.log(multiply(3, 5, 7));